package array_1D_log_pro;

public class Duplicate_Num {

	public static void main(String[] args) {
		
	int[] num = {1,2,3,3,2,4,5,1,4,8,0};
	
	
	for(int i=0; i<num.length; i++) {
		
		
	for(int j=i+1; j<num.length; j++)	{
		
		
		if (num[i] == num[j]) {
			
			System.out.println(num[j]);
		
		}
	}
	}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
